void
cleanUp(void)
{
    if (joystick) SDL_JoystickClose(joystick);
    if (renderer) SDL_DestroyRenderer(renderer);
    if (window) SDL_DestroyWindow(window);
    if (SDL_WasInit(0)) SDL_Quit();
}

